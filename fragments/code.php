<div>
	<p>
		A further explanation of the <strong>sample datasets</strong> we have
		available for testing ProseVis or getting started are described in the
		<a href="index.php?page=doc#sd" title="documentation">documentation</a>.
	</p>
	<p>
		The first dataset includes eight <a
			href="downloads/ShakespeareData.zip"
			title="Sample Documents">Shakespearean Plays</a>, compared "many to
		many" or each against each other. With this data set, users can see
		comparison data visualized on each play.
	</p>
	<p>
		The second dataset includes <a
			href="downloads/ThreeLivesData.zip"
			title="Sample Documents">Gertrude Stein's <em>Three Lives</em>
		</a> compared "one to many" or this one document compared against 10
		first person narratives from the Documenting the American South First
		Person Narratives Collection. With this data set, users can see
		comparison data visualized on the one document, but the other
		documents do not contain comparison data.
	</p>





